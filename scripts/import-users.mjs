// Email formula: Full Name -> lowercase, spaces->dots, strip non-email chars
// e.g. "Namrith Tejas Thatikonda" -> "namrith.tejas.thatikonda@padyalu.local"
function nameToEmail(fullName) {
  return fullName.trim().toLowerCase().replace(/s+/g, ".").replace(/[^a-z0-9._-]/g, "") + "@padyalu.local";
}

